package basicprograms;

public class AirthmaticalOperator {
	static void Add()
	{
		int a=100;
		int b=200;
		int c=a+b;
		System.out.println(c);	
	}
	static void Sub()
	{
		int a=100;
		int b=35;
		int c=a-b;
		System.out.println(c);	
	}
	static void Mul()
	{
		int a=45;
		int b=20;
		int c=a*b;
		System.out.println(c);	
	}
	static void Div()
	{
		int a=100;
		int b=25;
		int c=a/b;
		System.out.println(c);	
	}
	static void Mod()
	{
		int a=100;
		int b=22;
		int c=a%b;
		System.out.println(c);	
	}
	public static void main(String[] args)
	{
		Add();
		Sub();
		Mul();
		Div();
		Mod();

	}

}
