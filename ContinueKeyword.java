package javaprograms;

public class ContinueKeyword
{

	public static void main(String[] args) 
	{
	
       int i;
       for(i=1;i<11;i++)
       {
    	   if(i==7)
    	   {
    		   continue;
    	   }
    	   
    	   if(i==9)
    	   {
    		   continue;
    	   }
    		  
    	   
       
       System.out.println(i);
       }
	}

}
